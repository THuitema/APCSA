package com.thuitema.Unit_1;

public class September9 {

    public static void main(String[] args) {
        System.out.print("AP "); // Prints and cursor stops at end of line
        System.out.print("CS");
        System.out.print('A'); // Use single quotes for individual characters
        System.out.println(" Rocks!"); // Cursor goes to next line below

        System.out.println("Hello World\n");
        System.out.println("I \nlove\n java!");
        System.out.println("I \"love\" java! \n");

        System.out.println("/ \\ // \\\\ /// \\\\\\");

    }

}

/*
    Escape Sequences:
    \" - Double-quote
    \t - New tab
    \n - New line
    \\ - Backslash

*/
